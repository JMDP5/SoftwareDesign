    private void izborFajlaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_izborFajlaButtonActionPerformed
        int opcija = jFileChooser.showOpenDialog(this);
        File fajl = null;
        if (opcija == JFileChooser.APPROVE_OPTION) {
            fajl = jFileChooser.getSelectedFile();
            // TextFieldSlika.setText(fajl.getName());
            this.putanja = fajl.getAbsolutePath();
            TextFieldSlika.setText(fajl.getName());
            try {
                ucitanaSlika = ImageIO.read(fajl);
                imagePreview.setIcon(new ImageIcon(ucitanaSlika.getScaledInstance(200, 110, Image.SCALE_SMOOTH)));
            } catch (IOException ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_izborFajlaButtonActionPerformed

