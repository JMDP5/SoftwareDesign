    private void sacuvajZadatakBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sacuvajZadatakBtnActionPerformed

        if (!Validacija.daLiSuSvaPopunjena(getNazivZadatka(), getStatus(), getOpis(), rokTextField.getText())) {
            JOptionPane.showMessageDialog(this, "Molimo popunite sva polja!");
            return;
        }

        if (!Validacija.datumFormatOk(rokTextField.getText())) {
            JOptionPane.showMessageDialog(this, "Format datuma nije dobar!!");
            rokTextField.setBackground(Color.RED);
            return;
        }

        if (!Validacija.datumVeciOdDanas(rokTextField.getText())) {
            JOptionPane.showMessageDialog(this, "Datum ne sme biti manji od danasnjeg!!");
            rokTextField.setBackground(Color.RED);
            return;
        }

        trenutniZadatak = (Zadatak) kontroler.vratiOpstiDomenskiObjekat();
        trenutniZadatak.setNoveSlike(model.getSlike());
        try {

            kontroler.sacuvajZadatak();
        } catch (IOException | ClassNotFoundException ex) {
            Logger.getLogger(UnosZadatkaPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        model.setSlike(new ArrayList<Slika>());
    }//GEN-LAST:event_sacuvajZadatakBtnActionPerformed

    private void dodajUTabeluActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dodajUTabeluActionPerformed
        Slika slika = new Slika();
        slika.setZadatak(new Zadatak(Integer.parseInt(this.getIdTextField()), null, null, null, null, null, null, null));
        slika.setNaziv(TextFieldSlika.getText().trim());
        slika.setId(jTable1.getRowCount() + 1);
        slika.setSlika(ucitanaSlika);
        slika.setPutanjaDoSlike(putanja);
        model.dodajSliku(slika);
    }//GEN-LAST:event_dodajUTabeluActionPerformed


